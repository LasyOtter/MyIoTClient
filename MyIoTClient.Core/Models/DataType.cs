namespace MyIoTClient.Core.Models;

/// <summary>
/// 数据类型枚举
/// </summary>
public enum DataType
{
    /// <summary>
    /// 布尔值
    /// </summary>
    Bool,

    /// <summary>
    /// 字节
    /// </summary>
    Byte,

    /// <summary>
    /// 16位整数
    /// </summary>
    Int16,

    /// <summary>
    /// 32位整数
    /// </summary>
    Int32,

    /// <summary>
    /// 64位整数
    /// </summary>
    Int64,

    /// <summary>
    /// 无符号16位整数
    /// </summary>
    UInt16,

    /// <summary>
    /// 无符号32位整数
    /// </summary>
    UInt32,

    /// <summary>
    /// 无符号64位整数
    /// </summary>
    UInt64,

    /// <summary>
    /// 单精度浮点数
    /// </summary>
    Float,

    /// <summary>
    /// 双精度浮点数
    /// </summary>
    Double,

    /// <summary>
    /// 字符串
    /// </summary>
    String,

    /// <summary>
    /// 字节数组
    /// </summary>
    ByteArray
}
